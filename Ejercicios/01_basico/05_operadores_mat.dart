main() {
  //Operadores

  int a = 10 + 5; // + 15
  a = 20 - 10; // - 10
  a = 10 * 2; // * 20

  double b = 10 / 2; // / 5
  b = 10.0 % 3; // % 1 El sobrante de la división
  b = -b; // -expr Es usado para cambiar el valor de la expr

  int c = 10 ~/ 3; // ~/ 3 División comun y corriente pero solo la parte entera

  int d = 1;
  d++; // 2 Operador incremental
  d--; // 1 Operador decremental

  d += 2; // Le va a ir sumando de 2 en 2
  d -= 2; // Le va a ir quitando d 2 en 2
}
